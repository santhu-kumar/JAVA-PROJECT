
public class IncremantDemo {

	public static void main(String[] args) {
		
		int i=5;
		
		//pre increament example
		
		int preIncreament = i++;
		
		System.out.println("pre-Increament (i++) : ");
		System.out.println("original value of i :" +i);
		System.out.println("preIncreament value : " +preIncreament);
		

	}

}
